"""Task 6
Напишіть скрипт, який виводить на екран прямокутник із '*'. Висота і ширина прямокутника вводяться з клавіатури./
Наприклад, нижче представлений прямокутник з висотою 4 та шириною 5.
*****
*   *
*   *
*****"""

x = int(input('высота= '))
y = int(input('ширина= '))
i = 2
symb = '*'
otstup = ' ' * (y-2)
print(y * symb)
while i < x:
    print(symb, otstup, symb)
    i += 1
print(y * symb)