"""Task 5
Напишіть скрипт, який виводить на екран таблицю множення на 5. Переважно друкувати 1 x 5 = 5, 2 x 5 = 10, /
а не просто 5, 10, ..."""

x = int(input('таблицу умножения для какого числа хотел бы видеть? '))
y = int(input(f'таблицу умножения числа {x} до какого числа хотел бы видеть? '))
i = 1
while i <= y:
    print(f'{i} x {x} = {i*x}')
    i += 1